const fs = require('fs');
const path = require('path');

function readLines(filename) {
    return fs.readFileSync(path.join(__dirname, '../data', filename), 'utf8')
             .split('\n')
             .map(l => l.trim())
             .filter(Boolean);
}

const real = readLines('shuffled_real_wordles.txt');
const allowed = readLines('official_allowed_guesses.txt');
const combined = readLines('combined_wordlist.txt');

// produce the module
const out = `// THIS FILE IS AUTO-GENERATED BY /scripts/build-wordlists.js
export const realWordles = ${JSON.stringify(real, null, 2)};
export const allowedGuesses = ${JSON.stringify(allowed, null, 2)};
export const combinedWordList = ${JSON.stringify(combined, null, 2)};
`;

fs.writeFileSync(path.join(__dirname, '../src/wordlists.js'), out, 'utf8');